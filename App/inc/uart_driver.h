#ifndef __UART_DRIVER_H
#define __UART_DRIVER_H

#include "gd32h7xx.h"
#include <stdint.h>

//=================================================== 模块索引 ====================================================

typedef enum {
    UART_TOF    = 0,    /* TOF200F模块通信: UART3,    PA0-TX(J5-44) / PA1-RX(J5-43), AF8 */
    UART_DBG    = 1,    /* 调试串口:        UART4,    PB5-TX(J5-38) / PB13-RX(J5-36), AF14 → CH340 */
    UART_CAM    = 2,    /* 摄像头识别板:    UART7,    PC10-TX / PC11-RX (AF8), 已废弃 */
    UART_USART1 = 3,    /* 预留串口1:       USART1,   PA2-TX(J4-21) / PD6-RX(J4-23), AF7 */
    UART_UART6  = 4,    /* 预留串口2:       UART6,    PF7-TX(J4-17) / PF6-RX(J4-19), AF6 */
} uart_module_enum;

//=================================================== 接收缓冲区配置 ====================================================

#define UART_RX_BUF_SIZE    256     /* 每个UART模块的接收环形缓冲区大小（字节） */

//=================================================== 公开接口 ====================================================

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     UART模块初始化
// 参数说明     module      模块索引（UART_TOF 或 UART_DBG）
// 参数说明     baud        波特率（如 115200）
// 返回参数     void
// 使用示例     uart_init(UART_TOF, 115200);   //初始化UART3，波特率115200，使能中断接收
// 备注信息     初始化后自动使能接收中断（RBNE），接收到的数据存入软件FIFO
//-------------------------------------------------------------------------------------------------------------------
void uart_init(uart_module_enum module, uint32_t baud);

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     发送单个字节（阻塞模式）
// 参数说明     module      模块索引
// 参数说明     data        要发送的字节数据
// 返回参数     void
// 使用示例     uart_send_byte(UART_TOF, 0xA5);  //向TOF模块发送字节0xA5
// 备注信息     函数内部等待 TBE 标志位，发送完成才返回
//-------------------------------------------------------------------------------------------------------------------
void uart_send_byte(uart_module_enum module, uint8_t data);

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     发送字节数组（阻塞模式）
// 参数说明     module      模块索引
// 参数说明     buff        待发送数据的指针
// 参数说明     len         发送字节数
// 返回参数     void
// 使用示例     const uint8_t cmd[] = {0x01, 0x03, 0x00, 0x10, 0x00, 0x01, 0x85, 0xCF};
//              uart_send_buffer(UART_TOF, cmd, 8);   //发送8字节Modbus查询指令
//-------------------------------------------------------------------------------------------------------------------
void uart_send_buffer(uart_module_enum module, const uint8_t *buff, uint32_t len);

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     发送字符串（阻塞模式）
// 参数说明     module      模块索引
// 参数说明     str         以 '\0' 结尾的字符串指针
// 返回参数     void
// 使用示例     uart_send_string(UART_TOF, "Hello\r\n");  //发送字符串
//-------------------------------------------------------------------------------------------------------------------
void uart_send_string(uart_module_enum module, const char *str);

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     从软件FIFO中非阻塞读取一个字节
// 参数说明     module      模块索引
// 参数说明     dat         用于存储读取数据的指针
// 返回参数     uint8_t     1 = 读取成功，0 = 缓冲区为空
// 使用示例     uint8_t byte;
//              if (uart_query_byte(UART_TOF, &byte)) {
//                  printf("收到: %02X\r\n", byte);
//              }
// 备注信息     不直接访问硬件，从中断接收FIFO中读取
//-------------------------------------------------------------------------------------------------------------------
uint8_t uart_query_byte(uart_module_enum module, uint8_t *dat);

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     从软件FIFO中读取指定最大长度的数据
// 参数说明     module      模块索引
// 参数说明     buff        接收缓冲区指针
// 参数说明     max_len     最大读取字节数
// 返回参数     uint32_t    实际读取的字节数
// 使用示例     uint8_t rx_buf[32];
//              uint32_t len = uart_read_buffer(UART_TOF, rx_buf, sizeof(rx_buf));
//-------------------------------------------------------------------------------------------------------------------
uint32_t uart_read_buffer(uart_module_enum module, uint8_t *buff, uint32_t max_len);

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     清空软件接收FIFO
// 参数说明     module      模块索引
// 返回参数     void
// 使用示例     uart_flush_rx(UART_TOF);  //丢弃所有未读数据
// 备注信息     将读写指针同时归零，不清除缓冲区内容
//-------------------------------------------------------------------------------------------------------------------
void uart_flush_rx(uart_module_enum module);

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     获取软件FIFO中当前可读取的字节数
// 参数说明     module      模块索引
// 返回参数     uint32_t    缓冲区中已接收但未读取的字节数
// 使用示例     if (uart_rx_available(UART_TOF) >= 7) {
//                  // 至少有7字节，可能是一帧完整数据
//              }
//-------------------------------------------------------------------------------------------------------------------
uint32_t uart_rx_available(uart_module_enum module);

//-------------------------------------------------------------------------------------------------------------------
// 全局毫秒计数器 — 由 SysTick 中断服务函数递增，供帧超时检测使用
//-------------------------------------------------------------------------------------------------------------------
extern volatile uint32_t g_sys_ms;

#endif /* __UART_DRIVER_H */
