#ifndef GPS_H
#define GPS_H

#include "gd32h7xx.h"

//================================================== GPS 数据结构 ==================================================

typedef struct {
    /* ---- 定位状态 ---- */
    uint8_t     fix_valid;          /* 1=有效定位(A), 0=无效(V) */
    uint8_t     satellites;         /* 使用卫星数量 (0~12, 来自 GGA) */

    /* ---- 时间 (UTC) ---- */
    uint8_t     utc_hour;           /* UTC 时 (0~23) */
    uint8_t     utc_min;            /* UTC 分 (0~59) */
    uint8_t     utc_sec;            /* UTC 秒 (0~59) */
    uint8_t     utc_day;            /* UTC 日 (1~31) */
    uint8_t     utc_month;          /* UTC 月 (1~12) */
    uint8_t     utc_year;           /* UTC 年 (两位, 如 26 表示 2026) */

    /* ---- 位置 (十进制度) ---- */
    float       latitude;           /* 纬度 (度), 正=北纬, 负=南纬 */
    float       longitude;          /* 经度 (度), 正=东经, 负=西经 */

    /* ---- 运动 ---- */
    float       speed_ms;           /* 对地航速 (m/s) */
    float       course_deg;         /* 对地航向 (度, 真北参考) */

    /* ---- 内部 ---- */
    uint8_t     data_updated;       /* 本控制周期是否有新数据 (读取后清零) */
    uint32_t    last_fix_ms;        /* 最后一次收到有效 RMC 的毫秒时间戳 */
    uint32_t    sentence_count;     /* 累计收到的 NMEA 完整语句数 */
    uint32_t    rmc_count;          /* 累计收到的 RMC 语句数 */
    uint32_t    parse_errors;       /* 累计解析错误数 */
} GPS_Data;

/* 全局 GPS 数据实例 */
extern GPS_Data gps;

//================================================== 公开接口 ==================================================

//-------------------------------------------------------------------------------------------------------------------
// 函数名称     GPS_Init
// 函数说明     初始化 GPS 模块
//              依赖 SwUart_Init() 已提前调用（SW UART CH1 = 9600 bps）
//              重置 GPS 数据结构，打印模块信息
// 返回值       void
// 使用示例     GPS_Init();  // 在 System_Run() 中 SwUart_Init() 之后调用
//-------------------------------------------------------------------------------------------------------------------
void GPS_Init(void);

//-------------------------------------------------------------------------------------------------------------------
// 函数名称     GPS_Process
// 函数说明     GPS 运行时处理：从 SW UART CH1 接收缓冲区读取字节，送入 NMEA 解析器
//              应在主循环中每次迭代调用（50Hz 即可，GPS 数据更新率 1Hz）
//              解析到完整语句后自动更新全局 gps 结构体
// 返回值       void
// 使用示例     GPS_Process();  // 在主循环中调用
//-------------------------------------------------------------------------------------------------------------------
void GPS_Process(void);

//-------------------------------------------------------------------------------------------------------------------
// 函数名称     GPS_IsFixValid
// 函数说明     检查 GPS 定位是否有效
// 返回值       uint8_t    1=有效, 0=无效
// 使用示例     if (GPS_IsFixValid()) { printf("Lat=%.6f\r\n", gps.latitude); }
//-------------------------------------------------------------------------------------------------------------------
uint8_t GPS_IsFixValid(void);

//-------------------------------------------------------------------------------------------------------------------
// 函数名称     GPS_PrintStatus
// 函数说明     打印 GPS 当前状态到调试串口（单行摘要）
//              包含：定位标志、UTC时间、经纬度、速度、航向、卫星数
// 返回值       void
// 使用示例     GPS_PrintStatus();  // 1Hz 上报时调用
//-------------------------------------------------------------------------------------------------------------------
void GPS_PrintStatus(void);

#endif
