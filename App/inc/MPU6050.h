/**********************************************************************************************************************
 * 文件名          MPU6050.h
 * 描述            MPU6050 六轴传感器驱动头文件
 * 目标平台        GD32H7XXVET6
 * 接口            MyI2C (软件 I2C)
 *
 * 硬件接线定义：
 *                   ------------------------------------
 *                   模块管脚                    芯片管脚
 *                   VCC                       3.3V
 *                   GND                       GND
 *                   SCL                       PB10
 *                   SDA                       PB11
 *                   AD0                       GND (地址 0xD0, 悬空亦可)
 *                   INT                       未使用 (悬空)
 *                   ------------------------------------
 *
 * 说        明      所有传感器逻辑（初始化、校准、滤波、数据读取）均封装在本模块内，
 *                   main.c 只需调用高层 API 即可完成数据采集
 *
 * 量程配置（在 MPU6050.c 的 MPU6050_HardwareConfig 中修改）：
 *                   加速度：±2g   → 灵敏度 16384 LSB/g
 *                   陀螺仪：±250°/s → 灵敏度 131 LSB/(°/s)
 **********************************************************************************************************************/
#ifndef __MPU6050_H
#define __MPU6050_H

#include "gd32h7xx.h"

#define MPU6050_ADDRESS     0xD0        // MPU6050 I2C 器件地址 (AD0 接 GND 时使用此地址)

//-------------------------------------------------------------------------------------------------------------------
// MPU6050 六轴 + 温度数据结构体
//-------------------------------------------------------------------------------------------------------------------
typedef struct {
    int16_t AccX;       // X 轴加速度原始值（带符号）
    int16_t AccY;       // Y 轴加速度原始值
    int16_t AccZ;       // Z 轴加速度原始值
    int16_t Temp;       // 温度传感器原始值
    int16_t GyroX;      // X 轴陀螺仪原始值（已减零偏）
    int16_t GyroY;      // Y 轴陀螺仪原始值（已减零偏）
    int16_t GyroZ;      // Z 轴陀螺仪原始值（已减零偏）
} MPU6050_Data_t;

//-------------------------------------------------------------------------------------------------------------------
// 函数执行状态枚举
//-------------------------------------------------------------------------------------------------------------------
typedef enum {
    MPU6050_OK = 0,     // 操作成功
    MPU6050_ERROR = 1   // 操作失败（I2C 无应答、器件离线等）
} MPU6050_Status;

//================================================== 高层 API（main.c 直接调用） ==================================================

//-------------------------------------------------------------------------------------------------------------------
// 函数名称     MPU6050_Init
// 函数说明     完整初始化流程：I2C 初始化 → 硬件寄存器配置 → WHO_AM_I 校验 → 陀螺仪零偏校准 → 低通滤波初始化
// 返回值       MPU6050_OK = 传感器就绪, MPU6050_ERROR = 通信失败或 ID 不匹配
// 使用示例     if(MPU6050_Init() != MPU6050_OK) { /* 错误处理 */ }
// 备注信息     调用前需确保模块已上电且接线正确，校准期间模块必须保持静止不动
//-------------------------------------------------------------------------------------------------------------------
MPU6050_Status MPU6050_Init(void);

//-------------------------------------------------------------------------------------------------------------------
// 函数名称     MPU6050_Recalibrate
// 函数说明     陀螺仪重新校准 —— 串口收到 'c' 指令时调用，重新采集静止数据并更新零偏值
// 返回值       void
// 使用示例     MPU6050_Recalibrate();
// 备注信息     调用前必须确保模块处于完全静止状态，否则校准值不准
//-------------------------------------------------------------------------------------------------------------------
void MPU6050_Recalibrate(void);

//-------------------------------------------------------------------------------------------------------------------
// 函数名称     MPU6050_GetData
// 函数说明     从 MPU6050 一次性读取全部 14 字节数据（加速度 + 温度 + 陀螺仪），陀螺仪值自动减去已标定的零偏
// 参数         Data     存放读出数据的结构体指针
// 返回值       MPU6050_OK / MPU6050_ERROR
// 使用示例     MPU6050_Data_t data;  MPU6050_GetData(&data);
// 备注信息     读取频率由调用方控制，建议 100Hz（每 10ms 一次）
//-------------------------------------------------------------------------------------------------------------------
MPU6050_Status MPU6050_GetData(MPU6050_Data_t *Data);

//-------------------------------------------------------------------------------------------------------------------
// 函数名称     MPU6050_FilterApply
// 函数说明     对六轴数据应用一阶低通滤波（指数移动平均），消除短周期高频噪声
// 参数         Data     待滤波的数据结构体指针（原地修改）
// 返回值       void
// 使用示例     MPU6050_FilterApply(&data);
// 备注信息     须先在 MPU6050_Init() 中完成 FilterInit，滤波系数 alpha = 0.3
//-------------------------------------------------------------------------------------------------------------------
void MPU6050_FilterApply(MPU6050_Data_t *Data);

//================================================== 数据更新 & 对外读取接口 ==================================================

//-------------------------------------------------------------------------------------------------------------------
// 函数名称     MPU6050_Update
// 函数说明     执行一次完整的数据采集流程：读取 → 滤波 → 换算 → 缓存
//              调用后可通过下方的各个 Get 函数读取最新值
// 返回值       MPU6050_OK / MPU6050_ERROR
// 使用示例     if(MPU6050_Update() == MPU6050_OK) { x_mg = MPU6050_GetAccelX_mg(); }
// 备注信息     建议在主循环中以固定频率调用（如 100Hz）
//-------------------------------------------------------------------------------------------------------------------
MPU6050_Status MPU6050_Update(void);
uint8_t MPU6050_IsHealthy(void);                    /* 检测模块是否健康可用 */

//-------------------------------------------------------------------------------------------------------------------
// 函数名称     MPU6050_GetAccelX_mg / MPU6050_GetAccelY_mg / MPU6050_GetAccelZ_mg
// 函数说明     获取最近一次 MPU6050_Update 后的加速度值
// 返回值       加速度值，单位 mg（毫 g，即 1/1000 g）
// 使用示例     int32_t ax = MPU6050_GetAccelX_mg();  // Z 轴静止时约 1000
//-------------------------------------------------------------------------------------------------------------------
int32_t MPU6050_GetAccelX_mg(void);
int32_t MPU6050_GetAccelY_mg(void);
int32_t MPU6050_GetAccelZ_mg(void);

//-------------------------------------------------------------------------------------------------------------------
// 函数名称     MPU6050_GetGyroX_dps100 / MPU6050_GetGyroY_dps100 / MPU6050_GetGyroZ_dps100
// 函数说明     获取最近一次 MPU6050_Update 后的陀螺仪角速度值（已减零偏）
// 返回值       角速度值，单位 0.01 °/s（即返回值 ÷ 100 = 实际 °/s）
// 使用示例     int32_t gx = MPU6050_GetGyroX_dps100();  // 实际 °/s = gx / 100
//-------------------------------------------------------------------------------------------------------------------
int32_t MPU6050_GetGyroX_dps100(void);
int32_t MPU6050_GetGyroY_dps100(void);
int32_t MPU6050_GetGyroZ_dps100(void);

//-------------------------------------------------------------------------------------------------------------------
// 函数名称     MPU6050_GetTemp_c100
// 函数说明     获取最近一次 MPU6050_Update 后的芯片温度值
// 返回值       温度值，单位 0.01 °C（即返回值 ÷ 100 = 实际 °C）
// 使用示例     int32_t t = MPU6050_GetTemp_c100();  // 实际 °C = t / 100
// 备注信息     此值为 MPU6050 芯片内部温度，通常比环境温度高 2~5°C
//-------------------------------------------------------------------------------------------------------------------
int32_t MPU6050_GetTemp_c100(void);

//================================================== 物理量换算 ==================================================

//-------------------------------------------------------------------------------------------------------------------
// 函数名称     MPU6050_AccelToMg
// 函数说明     将加速度原始值换算为毫克 (mg)，系数与 HardwareConfig 中配置的量程自动匹配
// 参数         raw      加速度原始 ADC 值
// 返回值       换算后的加速度值，单位 mg
// 使用示例     int32_t mg = MPU6050_AccelToMg(data.AccX);
//-------------------------------------------------------------------------------------------------------------------
int32_t MPU6050_AccelToMg(int16_t raw);

//-------------------------------------------------------------------------------------------------------------------
// 函数名称     MPU6050_GyroToDps100
// 函数说明     将陀螺仪原始值换算为 0.01 °/s（即返回值 ÷ 100 = °/s）
// 参数         raw      陀螺仪原始 ADC 值（已减零偏）
// 返回值       换算后的角速度值，单位 0.01 °/s
// 使用示例     int32_t dps100 = MPU6050_GyroToDps100(data.GyroX);  // 实际 °/s = dps100 / 100
//-------------------------------------------------------------------------------------------------------------------
int32_t MPU6050_GyroToDps100(int16_t raw);

//-------------------------------------------------------------------------------------------------------------------
// 函数名称     MPU6050_TempToC100
// 函数说明     将温度原始值换算为 0.01 °C（即返回值 ÷ 100 = °C）
// 参数         raw      温度传感器原始 ADC 值
// 返回值       换算后的温度值，单位 0.01 °C
// 使用示例     int32_t c100 = MPU6050_TempToC100(data.Temp);  // 实际 °C = c100 / 100
//-------------------------------------------------------------------------------------------------------------------
int32_t MPU6050_TempToC100(int16_t raw);

//================================================== 底层 API（一般不需直接调用） ==================================================

MPU6050_Status MPU6050_WriteReg(uint8_t RegAddress, uint8_t Data);                       // 向指定寄存器写入一字节
MPU6050_Status MPU6050_ReadReg(uint8_t RegAddress, uint8_t *Data);                       // 从指定寄存器读取一字节
MPU6050_Status MPU6050_ReadRegs(uint8_t RegAddress, uint8_t *Data, uint8_t Length);      // 从指定寄存器连续读取多字节
MPU6050_Status MPU6050_GetID(uint8_t *ID);                                               // 读取 WHO_AM_I 寄存器
void MPU6050_CalibrateGyro(uint8_t samples);                                             // 陀螺仪零偏校准（需静止）
void MPU6050_GetGyroBias(int16_t *x, int16_t *y, int16_t *z);                            // 获取当前陀螺仪零偏值
void MPU6050_FilterInit(float alpha);                                                    // 初始化低通滤波器 (alpha: 0~1)

#endif
