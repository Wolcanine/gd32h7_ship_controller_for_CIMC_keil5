#ifndef PS2_H
#define PS2_H

#include "gd32h7xx.h"

/* ==================== PS2 手柄 SPI 引脚定义 ==================== */
/* 2026-06-10  适配 CIMC 核心板引出引脚 (J4/J5):
 *             PB14/PB15 核心板未引出 → DI 改 PA5, DO 改 PA7 */
#define PS2_DI_PORT   GPIOA      /* 数据输入 (MISO) — J5 Pin 42 */
#define PS2_DI_PIN    GPIO_PIN_5
#define PS2_DO_PORT   GPIOA      /* 数据输出 (MOSI) — J5 Pin 34 */
#define PS2_DO_PIN    GPIO_PIN_7
#define PS2_CS_PORT   GPIOB      /* 片选 — J4 Pin 59 */
#define PS2_CS_PIN    GPIO_PIN_12
#define PS2_CLK_PORT  GPIOB      /* 时钟 — J4 Pin 31 (原 PB13 让给 USART5 RX) */
#define PS2_CLK_PIN   GPIO_PIN_10

/* ==================== 手柄模式常量 ==================== */
#define PS2_MODE_DIGITAL  0x41   /* 数字模式 */
#define PS2_MODE_ANALOG   0x73   /* 模拟模式（摇杆+压感） */

/* ==================== 按键索引 ==================== */
#define PSB_SELECT      1
#define PSB_L3          2
#define PSB_R3          3
#define PSB_START       4
#define PSB_PAD_UP      5
#define PSB_PAD_RIGHT   6
#define PSB_PAD_DOWN    7
#define PSB_PAD_LEFT    8
#define PSB_L2          9
#define PSB_R2          10
#define PSB_L1          11
#define PSB_R1          12
#define PSB_TRIANGLE    13
#define PSB_CIRCLE      14
#define PSB_CROSS       15
#define PSB_SQUARE      16

/* ==================== PS2 数据结构体 ==================== */
typedef struct {
    uint8_t mode;
    uint8_t select:1;
    uint8_t l3:1;
    uint8_t r3:1;
    uint8_t start:1;
    uint8_t up:1;
    uint8_t right:1;
    uint8_t down:1;
    uint8_t left:1;
    uint8_t l2:1;
    uint8_t r2:1;
    uint8_t l1:1;
    uint8_t r1:1;
    uint8_t triangle:1;
    uint8_t circle:1;
    uint8_t cross:1;
    uint8_t square:1;
    uint8_t joy_left_x;
    uint8_t joy_left_y;
    uint8_t joy_right_x;
    uint8_t joy_right_y;
} PS2_Data_t;

extern PS2_Data_t PS2_Data;

/* ==================== PS2 公开 API ==================== */

//-------------------------------------------------------------------------------------------------------------------
// 函数名称     ps2_init
// 函数说明     初始化 PS2 手柄通信引脚：DI(输入), DO/CS/CLK(推挽输出)
//              配置后用 CS 高 / CLK 高 / DO 高作为 SPI 空闲状态
// 返回值       void
// 使用示例     ps2_init();  // 系统启动时调用一次
//-------------------------------------------------------------------------------------------------------------------
void ps2_init(void);

//-------------------------------------------------------------------------------------------------------------------
// 函数名称     ps2_read_data
// 函数说明     读取并解析一帧完整的 PS2 手柄数据（9 字节 SPI 帧）
//              解析结果存入全局 PS2_Data，同时更新断连检测状态
// 返回值       void
// 使用示例     ps2_read_data();  // 主循环中每次迭代调用 (50Hz)
// 备注信息     连续 PS2_FAIL_THRESHOLD(10) 帧无效则判定手柄离线
//-------------------------------------------------------------------------------------------------------------------
void ps2_read_data(void);

//-------------------------------------------------------------------------------------------------------------------
// 函数名称     ps2_key_pressed
// 函数说明     查询指定按键是否被按下
// 参数         key         按键索引 (PSB_SELECT / PSB_START / PSB_CROSS ...)
// 返回值       uint8_t     1 = 按下, 0 = 未按下
// 使用示例     if(ps2_key_pressed(PSB_CROSS)) { /* 执行动作 */ }
//-------------------------------------------------------------------------------------------------------------------
uint8_t ps2_key_pressed(uint8_t key);

//-------------------------------------------------------------------------------------------------------------------
// 函数名称     ps2_get_throttle
// 函数说明     获取左摇杆 Y 轴归一化油门值
//              摇杆上推 = 前进 (+1.0), 中位 = 停止 (0.0), 下拉 = 后退 (-1.0)
// 返回值       float       -1.0f ~ +1.0f (手柄离线时返回 0.0f)
// 使用示例     float thr = ps2_get_throttle();  pwm_set_left_duty(thr);
//-------------------------------------------------------------------------------------------------------------------
float ps2_get_throttle(void);

//-------------------------------------------------------------------------------------------------------------------
// 函数名称     ps2_get_steering
// 函数说明     获取左摇杆 X 轴归一化转向值
//              摇杆左推 = 左转 (-1.0), 中位 = 直行 (0.0), 右推 = 右转 (+1.0)
// 返回值       float       -1.0f ~ +1.0f (手柄离线时返回 0.0f)
// 使用示例     float steer = ps2_get_steering();  pwm_set_diff(steer);
//-------------------------------------------------------------------------------------------------------------------
float ps2_get_steering(void);

//-------------------------------------------------------------------------------------------------------------------
// 函数名称     ps2_get_mode
// 函数说明     获取当前手柄工作模式
// 返回值       uint8_t     PS2_MODE_DIGITAL (0x41=数字) 或 PS2_MODE_ANALOG (0x73=模拟)
//                         手柄离线时返回 PS2_MODE_DIGITAL
//-------------------------------------------------------------------------------------------------------------------
uint8_t ps2_get_mode(void);

//-------------------------------------------------------------------------------------------------------------------
// 函数名称     ps2_is_connected
// 函数说明     查询手柄是否在线
// 返回值       uint8_t     1 = 在线, 0 = 离线
//-------------------------------------------------------------------------------------------------------------------
uint8_t ps2_is_connected(void);

//-------------------------------------------------------------------------------------------------------------------
// 函数名称     ps2_print_state
// 函数说明     通过调试串口输出完整的 PS2 手柄状态（D-Pad、动作键、肩键、摇杆值）
//              用于遥控器模块独立测试和调试
// 返回值       void
// 备注信息     调用前需确保 uart_init(UART_DBG, 115200) 已完成
//-------------------------------------------------------------------------------------------------------------------
void ps2_print_state(void);

#endif
