/**********************************************************************************************************************
 * 文件名          MyI2C.h
 * 描述            软件 I2C 驱动头文件 —— 引脚配置 & API 声明
 * 目标平台        GD32H7XXVET6
 * 实现方式        GPIO 位操作模拟 I2C 时序（开漏输出 + 上拉电阻）
 *
 * 默认接线：
 *                   ------------------------------------
 *                   信号线                      芯片管脚
 *                   SCL                        PB10
 *                   SDA                        PB11
 *                   ------------------------------------
 *
 * 使用方法：
 *   1. 如需更改 I2C 引脚，只修改下方 5 个宏定义即可
 *   2. 连接多个 I2C 从机设备时，共用本模块，靠不同器件地址区分
 *   3. 需要第二路独立 I2C 总线时，复制本文件重命名，修改宏为新引脚即可
 **********************************************************************************************************************/
#ifndef __MYI2C_H
#define __MYI2C_H

#include "gd32h7xx.h"

//================================================== 引脚配置（可修改） ==================================================
// 更改 I2C 总线引脚时，只需修改下面 5 个宏
#define MYI2C_GPIO_PORT     GPIOB           // SCL / SDA 所在 GPIO 端口
#define MYI2C_SCL_PIN       GPIO_PIN_10     // SCL 时钟引脚
#define MYI2C_SDA_PIN       GPIO_PIN_11     // SDA 数据引脚
#define MYI2C_RCU_CLK       RCU_GPIOB       // GPIO 端口时钟
//================================================== 引脚配置结束 ==================================================

//-------------------------------------------------------------------------------------------------------------------
// 函数名称     MyI2C_Init
// 函数说明     初始化 I2C 总线引脚（开漏输出，释放总线至高电平）
// 返回值       void
// 使用示例     MyI2C_Init();
//-------------------------------------------------------------------------------------------------------------------
void MyI2C_Init(void);

//-------------------------------------------------------------------------------------------------------------------
// 函数名称     MyI2C_Start
// 函数说明     发送 I2C 起始条件：SCL 高电平期间 SDA 由高变低
// 返回值       void
//-------------------------------------------------------------------------------------------------------------------
void MyI2C_Start(void);

//-------------------------------------------------------------------------------------------------------------------
// 函数名称     MyI2C_Stop
// 函数说明     发送 I2C 停止条件：SCL 高电平期间 SDA 由低变高
// 返回值       void
//-------------------------------------------------------------------------------------------------------------------
void MyI2C_Stop(void);

//-------------------------------------------------------------------------------------------------------------------
// 函数名称     MyI2C_SendByte
// 函数说明     向 I2C 总线发送一个字节（高位先行 MSB first）
// 参数         Byte     待发送的 8 位数据
// 返回值       void
//-------------------------------------------------------------------------------------------------------------------
void MyI2C_SendByte(uint8_t Byte);

//-------------------------------------------------------------------------------------------------------------------
// 函数名称     MyI2C_ReceiveByte
// 函数说明     从 I2C 总线接收一个字节（高位先行 MSB first），主机释放 SDA
// 返回值       接收到的 8 位数据
//-------------------------------------------------------------------------------------------------------------------
uint8_t MyI2C_ReceiveByte(void);

//-------------------------------------------------------------------------------------------------------------------
// 函数名称     MyI2C_SendAck
// 函数说明     主机发送应答信号给从机
// 参数         AckBit   0 = ACK（继续传输），1 = NACK（停止传输）
// 返回值       void
//-------------------------------------------------------------------------------------------------------------------
void MyI2C_SendAck(uint8_t AckBit);

//-------------------------------------------------------------------------------------------------------------------
// 函数名称     MyI2C_ReceiveAck
// 函数说明     主机接收从机应答信号
// 返回值       0 = ACK（从机应答正常），1 = NACK（从机无应答 / 通信异常）
//-------------------------------------------------------------------------------------------------------------------
uint8_t MyI2C_ReceiveAck(void);

#endif
