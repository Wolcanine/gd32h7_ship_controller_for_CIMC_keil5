/**********************************************************************************************************************
 * 文件名          MPU6050_Reg.h
 * 描述            MPU6050 寄存器地址宏定义（数据手册 Register Map）
 * 目标平台        GD32H7XXVET6
 **********************************************************************************************************************/
#ifndef __MPU6050_REG_H
#define __MPU6050_REG_H

//================================================== 配置寄存器 ==================================================

#define MPU6050_SMPLRT_DIV      0x19    // 采样率分频：采样率 = 陀螺仪输出频率 / (1 + SMPLRT_DIV)
#define MPU6050_CONFIG          0x1A    // 低通滤波配置：DLPF_CFG[2:0]，值越大截止频率越低
#define MPU6050_GYRO_CONFIG     0x1B    // 陀螺仪量程配置：FS_SEL[4:3]
                                        //   0x00 → ±250 °/s   (灵敏度 131  LSB/°/s)
                                        //   0x08 → ±500 °/s   (灵敏度 65.5 LSB/°/s)
                                        //   0x10 → ±1000 °/s  (灵敏度 32.8 LSB/°/s)
                                        //   0x18 → ±2000 °/s  (灵敏度 16.4 LSB/°/s)
#define MPU6050_ACCEL_CONFIG    0x1C    // 加速度量程配置：AFS_SEL[4:3]
                                        //   0x00 → ±2g   (灵敏度 16384 LSB/g)
                                        //   0x08 → ±4g   (灵敏度 8192  LSB/g)
                                        //   0x10 → ±8g   (灵敏度 4096  LSB/g)
                                        //   0x18 → ±16g  (灵敏度 2048  LSB/g)

//================================================== 数据输出寄存器 ==================================================

#define MPU6050_ACCEL_XOUT_H    0x3B    // 加速度 X 轴高字节
#define MPU6050_ACCEL_XOUT_L    0x3C    // 加速度 X 轴低字节
#define MPU6050_ACCEL_YOUT_H    0x3D    // 加速度 Y 轴高字节
#define MPU6050_ACCEL_YOUT_L    0x3E    // 加速度 Y 轴低字节
#define MPU6050_ACCEL_ZOUT_H    0x3F    // 加速度 Z 轴高字节
#define MPU6050_ACCEL_ZOUT_L    0x40    // 加速度 Z 轴低字节
#define MPU6050_TEMP_OUT_H      0x41    // 温度传感器高字节
#define MPU6050_TEMP_OUT_L      0x42    // 温度传感器低字节
#define MPU6050_GYRO_XOUT_H     0x43    // 陀螺仪 X 轴高字节
#define MPU6050_GYRO_XOUT_L     0x44    // 陀螺仪 X 轴低字节
#define MPU6050_GYRO_YOUT_H     0x45    // 陀螺仪 Y 轴高字节
#define MPU6050_GYRO_YOUT_L     0x46    // 陀螺仪 Y 轴低字节
#define MPU6050_GYRO_ZOUT_H     0x47    // 陀螺仪 Z 轴高字节
#define MPU6050_GYRO_ZOUT_L     0x48    // 陀螺仪 Z 轴低字节

//================================================== 电源管理 / 状态寄存器 ==================================================

#define MPU6050_PWR_MGMT_1      0x6B    // 电源管理 1：复位(bit7)、睡眠(bit6)、时钟源选择(bit2:0)
                                        //   0x80 → 器件复位
                                        //   0x01 → 唤醒，PLL X 轴陀螺仪作为时钟源
#define MPU6050_PWR_MGMT_2      0x6C    // 电源管理 2：各轴使能控制
                                        //   0x00 → 加速度计和陀螺仪全部 6 轴开启
#define MPU6050_WHO_AM_I        0x75    // 器件 ID 寄存器（只读，默认值 0x68）

#endif
