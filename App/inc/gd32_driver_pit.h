/*!
 *  \file    gd32_driver_pit.h
 *  \brief   PIT (Periodic Interrupt Timer) 模块
 *
 *  \version 2026-05-06, V1.0.0
 *
 *  基于 GD32H7XX 定时器更新中断的周期性中断模块。
 *  初始化后定时器按指定周期触发更新中断，用户自行编写对应的
 *  TIMERx_IRQHandler 处理中断逻辑。
 *
 *  支持 TIMER1 ~ TIMER6 (均拥有独立中断号)。
 *  TIMER0 / TIMER7 因中断号与其他外设共享，暂不纳入。
 *
 *  使用示例:
 *    pit_ms_init(PIT_TIMER4, 1);      // TIMER4 每 1ms 触发一次中断
 *    pit_init(PIT_TIMER2, 168000);    // TIMER2 每 168000 个时钟周期触发
 */

#ifndef _GD32_DRIVER_PIT_H_
#define _GD32_DRIVER_PIT_H_

#include "gd32h7xx.h"

/* ---------- 定时器索引枚举 (仅含独立中断号的定时器) ---------- */
/* 注: TIMER4 已被 gd32_driver_pwm 软件 PWM 模块独占使用，
 *     请勿将 PIT_TIMER4 与 PWM 模块同时使用！              */
typedef enum
{
    PIT_TIMER1 = 0,
    PIT_TIMER2 = 1,
    PIT_TIMER3 = 2,
    PIT_TIMER4 = 3,         /* ⚠ 已被 gd32_driver_pwm 占用 */
    PIT_TIMER5 = 4,         /* 中断号 TIMER5_DAC_UDR_IRQn (H7) */
    PIT_TIMER6 = 5,
} pit_index_enum;

//---------------------------------------------------------------------------------------------------------------------
// 函数名:   pit_init
// 描述:     PIT 初始化（period 为 SystemCoreClock 周期数）
// 参数:     pit_n       PIT 定时器编号
//           period      中断间隔（SystemCoreClock 时钟周期数）
// 返回值:   void
// 示例:     pit_init(PIT_TIMER4, SystemCoreClock / 1000);   // 1ms 间隔
// 备注:     自动计算预分频与重装载值，使能更新中断并启动定时器
//---------------------------------------------------------------------------------------------------------------------
void pit_init(pit_index_enum pit_n, uint32_t period);

//---------------------------------------------------------------------------------------------------------------------
// 函数名:   pit_enable
// 描述:     使能 PIT 中断
// 参数:     pit_n       PIT 定时器编号
// 返回值:   void
//---------------------------------------------------------------------------------------------------------------------
void pit_enable(pit_index_enum pit_n);

//---------------------------------------------------------------------------------------------------------------------
// 函数名:   pit_disable
// 描述:     禁止 PIT 中断（定时器继续运行，仅关闭中断）
// 参数:     pit_n       PIT 定时器编号
// 返回值:   void
//---------------------------------------------------------------------------------------------------------------------
void pit_disable(pit_index_enum pit_n);

//================================================== 宏定义函数 ==================================================
//---------------------------------------------------------------------------------------------------------------------
// 函数名:   pit_ms_init
// 描述:     PIT 毫秒级初始化
// 参数:     pit_n       定时器编号
//           ms          中断间隔 (ms)
// 示例:     pit_ms_init(PIT_TIMER4, 1);   // 每 1ms 中断一次
//---------------------------------------------------------------------------------------------------------------------
#define pit_ms_init(pit_n, ms)  (pit_init((pit_n), (uint32_t)((ms) * (SystemCoreClock / 1000))))

//---------------------------------------------------------------------------------------------------------------------
// 函数名:   pit_us_init
// 描述:     PIT 微秒级初始化
// 参数:     pit_n       定时器编号
//           us          中断间隔 (us)
// 示例:     pit_us_init(PIT_TIMER4, 100); // 每 100us 中断一次
//---------------------------------------------------------------------------------------------------------------------
#define pit_us_init(pit_n, us)  (pit_init((pit_n), (uint32_t)((us) * (SystemCoreClock / 1000000))))

//================================================== 宏定义函数 ==================================================

#endif /* _GD32_DRIVER_PIT_H_ */
