#ifndef __VISION_H
#define __VISION_H

#include "gd32h7xx.h"
#include <stdint.h>

//=================================================== 检测目标类型 ====================================================

typedef enum {
    VISION_CLASS_NONE       = 0,        /* 未识别 / 背景                           */
    VISION_CLASS_GARBAGE    = 1,        /* 水面漂浮垃圾（通用）                     */
    VISION_CLASS_BOTTLE     = 2,        /* 塑料瓶                                   */
    VISION_CLASS_BAG        = 3,        /* 塑料袋                                   */
    VISION_CLASS_LEAF       = 4,        /* 大片落叶 / 水草                          */
    VISION_CLASS_OBSTACLE   = 5,        /* 障碍物（浮木、石头等，需避让而非抓取）   */
} VisionClass;

//=================================================== 检测结果数据结构 ====================================================

/* 单个检测目标 */
typedef struct {
    float       x_mm;           /* 目标在船体坐标系中的 X 位置 (mm)，船头方向为 +Y，右舷为 +X */
    float       y_mm;           /* 目标在船体坐标系中的 Y 位置 (mm)                           */
    float       width_mm;       /* 目标近似宽度 (mm)                                         */
    uint8_t     class;          /* 目标类别，参考 VisionClass 枚举                           */
    uint8_t     confidence;     /* 置信度 0~100，100=最高置信                                */
} VisionTarget;

#define VISION_MAX_TARGETS  8       /* 单帧最多检测目标数 */

/* 一帧完整的检测结果 */
typedef struct {
    uint8_t         count;                          /* 本帧检测到的目标数量 (0~VISION_MAX_TARGETS) */
    VisionTarget    targets[VISION_MAX_TARGETS];    /* 目标列表，按距离由近到远排序               */
    uint32_t        frame_id;                       /* 帧序号，每处理一帧递增                     */
    uint32_t        timestamp_ms;                   /* 本帧时间戳 (g_sys_ms)                      */
} VisionFrame;

//=================================================== 公开接口 ====================================================

//-------------------------------------------------------------------------------------------------------------------
// 函数名称     Vision_Init
// 函数说明     初始化摄像头硬件 + 配置采集参数
//             待另一个工程提供硬件接口后填充具体实现
// 参数         void
// 返回值       void
// 使用示例     Vision_Init();  // 系统启动时调用一次
// 备注信息     TODO: 待视觉工程接口确定后，在此处初始化 DCI/I2C/摄像头寄存器
//-------------------------------------------------------------------------------------------------------------------
void Vision_Init(void);

//-------------------------------------------------------------------------------------------------------------------
// 函数名称     Vision_Update
// 函数说明     采集一帧图像并执行目标检测（当前为占位实现）
//             由 main.c 每控制周期 (50Hz) 调用一次
//             实际每 N 帧执行一次完整检测（如 5Hz），其余帧跳过
// 参数         void
// 返回值       void
// 使用示例     // 在主循环中：Vision_Update();
// 备注信息     TODO: 对接视觉工程后替换为实际采集+推理流程
//-------------------------------------------------------------------------------------------------------------------
void Vision_Update(void);

//-------------------------------------------------------------------------------------------------------------------
// 函数名称     Vision_GetFrame
// 函数说明     获取最近一帧的完整检测结果（只读指针）
// 参数         void
// 返回值       const VisionFrame*  检测结果指针；无有效数据时返回 NULL
// 使用示例     const VisionFrame *f = Vision_GetFrame();
//              if (f && f->count > 0) { ... }
//-------------------------------------------------------------------------------------------------------------------
const VisionFrame* Vision_GetFrame(void);

//-------------------------------------------------------------------------------------------------------------------
// 函数名称     Vision_HasTarget
// 函数说明     快速判断是否有检测到目标
// 参数         void
// 返回值       uint8_t   1 = 有目标, 0 = 无目标
// 使用示例     if (Vision_HasTarget()) { ... }
//-------------------------------------------------------------------------------------------------------------------
uint8_t Vision_HasTarget(void);

//-------------------------------------------------------------------------------------------------------------------
// 函数名称     Vision_GetClosestTarget
// 函数说明     获取距离船最近的有效目标（用于导航引导 + 机械臂抓取）
//             按 y_mm 排序，返回最近的；同类垃圾中取最近者
// 参数         void
// 返回值       VisionTarget  最近目标；无目标时 confidence=0
// 使用示例     VisionTarget t = Vision_GetClosestTarget();
//              if (t.confidence > 50) { steer_toward(t.x_mm); }
//-------------------------------------------------------------------------------------------------------------------
VisionTarget Vision_GetClosestTarget(void);

//-------------------------------------------------------------------------------------------------------------------
// 函数名称     Vision_IsReady
// 函数说明     查询视觉模块是否已完成初始化并可正常使用
// 参数         void
// 返回值       uint8_t   1 = 已就绪, 0 = 未初始化或初始化失败
//-------------------------------------------------------------------------------------------------------------------
uint8_t Vision_IsReady(void);

#endif /* __VISION_H */
