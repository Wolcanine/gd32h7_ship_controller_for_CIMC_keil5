#ifndef LASER_H
#define LASER_H

#include "gd32h7xx.h"

//================================================== 返回值定义 ==================================================

#define LASER_READ_ERROR   0xFFFF      // 激光读错误返回值 (传感器未就绪或通信失败)
#define LASER_INVALID      0           // 无效读数 (距离数据不可用)

//================================================== 公开接口 ==================================================

//-------------------------------------------------------------------------------------------------------------------
// 函数名称     Laser_Init
// 函数说明     初始化 TOF200F 激光测距模块
//              流程：UART3 初始化 (PA0/PA1, 115200) → 握手 → 高精度模式配置
//              内部调用 uart_init(UART_TOF, 115200)，无需外部提前初始化
// 返回值       void
// 使用示例     Laser_Init();  // 在 main() 中所有模块初始化阶段调用一次即可
// 备注信息     初始化失败不阻塞（仅打印日志），后续 Laser_GetDistanceCm() 将返回 LASER_READ_ERROR
//-------------------------------------------------------------------------------------------------------------------
void Laser_Init(void);

//-------------------------------------------------------------------------------------------------------------------
// 函数名称     Laser_GetDistanceCm
// 函数说明     获取最近一次成功读取的激光测距值，并触发新一轮查询
//              内部自动完成：接收缓冲区排空 → 帧超时清理 → 周期查询触发 (200ms 间隔)
// 返回值       uint16_t    距离值，单位 cm
//                          返回 LASER_READ_ERROR (0xFFFF) 表示传感器未就绪或通信失败
//                          返回 LASER_INVALID (0) 表示读数无效
// 使用示例     uint16_t dist = Laser_GetDistanceCm();
//             if(dist != LASER_READ_ERROR && dist != LASER_INVALID) { printf("dist=%u cm\r\n", dist); }
// 备注信息     建议在主循环中以固定频率调用（如 50Hz），函数内部自动管理查询间隔
//-------------------------------------------------------------------------------------------------------------------
uint16_t Laser_GetDistanceCm(void);

#endif
